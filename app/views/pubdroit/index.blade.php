					<div class="product-longdesc">
						<h3>Etude des moyens d'action en droit suisse</h3>
						<p class="align-justify">Les données personnelles médicales sont des informations essentielles que les 
						professionnels de la santé doivent détenir pour accomplir leurs tâches; si elles sont indispensables, 
						elles sont également très sensibles, car elles portent très souvent sur des aspects intimes du patient, 
						qui ne doivent pas être portés à la connaissance de tout un chacun. Elles doivent par conséquent bénéficier 
						d'une protection efficace, tant juridique que technique. </p>
						<p class="align-justify">Outre les dispositions prévues par le droit international, la législation suisse 
						leur confère également une protection en droit civil, en droit public ou encore en droit pénal. 
						Cette thèse a ainsi pour but d'examiner ces différents moyens de protection, en particulier les 
						actions défensives du Code civil, les actions réparatrices du Code des obligations, les devoirs de 
						confidentialité (secret professionnel, secret de fonction, devoir de discrétion notamment) et les 
						mesures que le Préposé fédéral à la protection des données et à la transparence peut prendre. 
						Une fois ces différents moyens juridiques exposés, se pose alors la question de leur efficacité. 
						En effet, en raison de l'évolution des nouvelles technologies et d'Internet, on peut se demander 
						si les dispositions actuelles sont encore suffisantes aujourd'hui et si les données personnelles 
						médicales ne devraient pas bénéficier d'une meilleure protection.</p>
						<p class="align-justify">Cette thèse examine ainsi les lacunes inhérentes à ces différents moyens juridiques et propose pour 
						chacun des solutions pouvant améliorer la protection des données personnelles médicales.</p>
